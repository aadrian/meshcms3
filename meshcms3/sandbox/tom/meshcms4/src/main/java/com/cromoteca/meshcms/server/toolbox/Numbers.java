package com.cromoteca.meshcms.server.toolbox;

public class Numbers {
	public static int comparisonSign(long l1, long l2) {
		long l = l1 - l2;

		if (l < 0) {
			return -1;
		}

		if (l > 0) {
			return 1;
		}

		return 0;
	}
}
